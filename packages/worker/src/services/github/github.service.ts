const github = axios.create({
  baseURL: 'https://api.github.com',
  headers: {
    Authorization: `Basic ${process.env.GITHUB}`,
  },
});

export class GithubService {
  static async loadPullRequests(): Promise<PullsList[]> {
    const names = await GithubService.loadRepositories();
    const pulls = [];
    for (const name of names) {
      try {
        const info = await GithubService.loadPulls(name);
        pulls.push(...info);
      } catch (err) {
        console.log('not found');
      }
    }

    return pulls;
  }

  static async GithubProfileInformation(githubName: string): Promise<GithubInformation> {
    const { data } = await github.get(`/users/${githubName}`);
    return data;
  }

  static async loadRepositories(): Promise<string[]> {
    const { data } = await github.get('/orgs/novuhq/repos?type=public');
    return data.map((d: any) => d.name);
  }

  static async loadPulls(name: string, page = 1): Promise<PullsList[]> {
    const path = `/repos/novuhq/${name}/pulls?state=closed&per_page=30&page=${page}`;
    console.log(`- Getting: ${path}`);
    const { data } = await github.get(path);
    return [
      ...data,
      ...(data.length ? await GithubService.loadPulls(name, page + 1) : []),
    ];
  }
}
